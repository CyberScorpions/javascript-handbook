// Strings can have single or double quotes, does not matter (single is more modern)
// Semi colons are not mandatory (no errors will be given), but best practice is to use them
// There is no float/decimal data type in JavaScript
// Null basically means empty; it's a variable but there is nothing in it

const name = 'John';    // string
const age = 30;         // number
const rating = 4.5;     // number
const isCool = true;    // boolean
const x = null;         // null
